package com.example.bledos.model;

public class BengkelModel {

    private String kode_bengkel;

    private String nama_bengkel;

    private String alamat_bengkel;

    private double latitude;

    private double longitude;

    private String phonenumber;

    public String getKode_bengkel() {
        return kode_bengkel;
    }

    public String getNama_bengkel() {
        return nama_bengkel;
    }

    public String getAlamat_bengkel() {
        return alamat_bengkel;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public String getPhonenumber() {
        return phonenumber;
    }
}
